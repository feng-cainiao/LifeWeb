package newswebsite.pojo;

public class ApproveComment {
    private Integer approvecommentId;

    private Integer commentId;

    private Integer userId;

    public Integer getApprovecommentId() {
        return approvecommentId;
    }

    public void setApprovecommentId(Integer approvecommentId) {
        this.approvecommentId = approvecommentId;
    }

    public Integer getCommentId() {
        return commentId;
    }

    public void setCommentId(Integer commentId) {
        this.commentId = commentId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }
}